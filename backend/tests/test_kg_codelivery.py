@needs_db
def test_co_delivery_mined_and_gated(client: TestClient) -> None:
    """The corpus yields real ``co_delivered`` proposals, and every one PASSED G1-G8 (nothing
    ungated leaks) — a dashed pending_edge + a Change-Flags item, never a live kg_edge."""
    with _sync().connect() as c:
        codel = c.execute(
            text(
                "SELECT count(*) FROM control.pending_edge "
                "WHERE version_id = 'v7' AND kind = 'co_delivered' AND status = 'pending'"
            )
        ).scalar_one()
        flags = c.execute(
            text(
                "SELECT count(*) FROM control.change_flag "
                "WHERE kind = 'kg_edge_proposal' AND detail->>'kind_edge' = 'co_delivered'"
            )
        ).scalar_one()
        passed = c.execute(
            text(
                "SELECT count(*) FROM control.change_flag cf "
                "JOIN control.validation_gate_run g ON g.chain_id = cf.chain_id "
                "WHERE cf.kind = 'kg_edge_proposal' "
                "AND cf.detail->>'kind_edge' = 'co_delivered' AND g.verdict = 'pass'"
            )
        ).scalar_one()
    assert codel > 0, "expected co-delivery proposals mined from the Jira corpus"
    assert flags == codel and passed == flags  # every co_delivered proposal is gated, all passed


