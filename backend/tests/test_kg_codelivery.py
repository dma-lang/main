@needs_db
def test_co_delivery_reconciles_with_corpus(client: TestClient) -> None:
    """Grounded, not fabricated: a mined co_delivered edge's ``co_stories`` count matches a direct
    same-story co-occurrence count over the real corpus (control.story_catalogue_link)."""
    with _sync().connect() as c:
        row = (
            c.execute(
                text(
                    "SELECT fn.ref_id AS a, tn.ref_id AS b, "
                    "(pe.detail->>'co_stories')::int AS co_stories "
                    "FROM control.pending_edge pe "
                    "JOIN control.kg_node fn ON fn.node_id = pe.from_node "
                    "JOIN control.kg_node tn ON tn.node_id = pe.to_node "
                    "WHERE pe.version_id = 'v7' AND pe.kind = 'co_delivered' "
                    "AND (pe.detail->>'co_stories')::int > 0 "
                    "ORDER BY (pe.detail->>'co_stories')::int DESC LIMIT 1"
                )
            )
            .mappings()
            .first()
        )
        assert row is not None, "expected a same-story co-delivery pair to reconcile"
        actual = c.execute(
            text(
                "SELECT count(DISTINCT l1.story_key) FROM control.story_catalogue_link l1 "
                "JOIN control.story_catalogue_link l2 ON l2.story_key = l1.story_key "
                "WHERE l1.version_id = 'v7' AND l2.version_id = 'v7' "
                "AND l1.subcap_id = :a AND l2.subcap_id = :b"
            ),
            {"a": row["a"], "b": row["b"]},
        ).scalar_one()
    assert actual == row["co_stories"]  # the basis is the real corpus count, not a guess


@needs_db
def test_discover_ranks_cross_pillar_novelty_first(client: TestClient) -> None:
    """The headline: /kg/discover ranks the strong-AND-non-obvious links first. The #1 relationship
    "you may be missing" is a CROSS-PILLAR co-delivery bond (high novelty), and the payload is
    monotonically novelty-descending — obvious same-pillar siblings never lead."""
    disc = client.get("/api/catalogue/v7/kg/discover?limit=20").json()
    latent = disc["latent"]
    assert latent, "discovery surface should not be empty"
    novelties = [e["novelty"] for e in latent]
    assert novelties == sorted(novelties, reverse=True)  # ranked by novelty
    top = latent[0]
    assert top["crosses"] == "cross_pillar"  # strong-but-not-obvious leads
    assert top["novelty"] > 0.5 and top["basis"] and top["pending_id"]


@needs_db
def test_ego_graph_weights_and_explains_edges(client: TestClient) -> None:
    """The per-subcap KG carries weighted, explained edges + a per-subcap latent panel. Pick a
    subcap that centres a co_delivered proposal and assert its dashed edge has strength + basis +
    crosses, and the ego "you may be missing" panel is populated."""
    with _sync().connect() as c:
        sid = c.execute(
            text(
                "SELECT fn.ref_id FROM control.pending_edge pe "
                "JOIN control.kg_node fn ON fn.node_id = pe.from_node "
                "WHERE pe.version_id = 'v7' AND pe.kind = 'co_delivered' LIMIT 1"
            )
        ).scalar_one()
    kg = client.get(f"/api/catalogue/v7/kg?subcap={sid}").json()
    codel = [e for e in kg["pending"] if e["kind"] == "co_delivered"]
    assert codel, "expected a dashed co_delivered edge on this subcap"
    e = codel[0]
    assert e["strength"] and e["basis"] and e["crosses"] and e["layer"] == "B_proposed"
    assert kg["latent"], "ego 'relationships you may be missing' panel should be populated"
    # Layer-A now also projects offering / value-chain siblings, each with its own strength + basis.
    assert any(x["kind"] == "same_value_chain" and x["strength"] for x in kg["edges"]) or any(
        x["kind"] == "shares_offering" and x["strength"] for x in kg["edges"]
    )


@needs_db
def test_approve_promotes_co_delivered_keeping_its_why(client: TestClient) -> None:
    """Approving a co_delivered proposal re-gates and promotes its pending_edge to an accepted
    Layer-B kg_edge that STILL carries its basis (migration 0014 detail) — an accepted edge can
    always explain itself."""
    flags = client.get("/api/change-flags?status=open").json()["flags"]
    f = next(
        x for x in flags if x["kind"] == "kg_edge_proposal" and str(x["target"]).endswith(":cd")
    )
    approved = client.post(f"/api/change-flags/{f['id']}/approve").json()
    assert approved["resolved"] is True and approved["status"] == "approved"
    with _sync().connect() as c:
        edge = (
            c.execute(
                text(
                    "SELECT detail FROM control.kg_edge "
                    "WHERE version_id = 'v7' AND kind = 'co_delivered' "
                    "ORDER BY edge_id DESC LIMIT 1"
                )
            )
            .mappings()
            .first()
        )
    assert edge is not None
    detail = edge["detail"] if isinstance(edge["detail"], dict) else json.loads(edge["detail"])
    assert detail and detail.get("basis") and detail.get("kind_edge") == "co_delivered"


